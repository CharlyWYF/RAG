def t(key: str, **kwargs) -> str:
    """Translate a key. Falls back to the key itself if not found."""
    text = _ACTIVE.get(key, _ZH.get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError):
            return text
    return text


def current_lang() -> str:
    return "en" if _ACTIVE is _EN else "zh"


def set_lang(lang: str) -> None:
    """Switch active language at runtime."""
    global _ACTIVE, _LANG
    _LANG = lang.strip().lower()
    _ACTIVE = _DICTS.get(_LANG, _ZH)
